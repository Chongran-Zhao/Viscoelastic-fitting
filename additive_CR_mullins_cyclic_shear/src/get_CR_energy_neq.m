function out = get_CR_energy_neq(mu_neq, m_neq, n_neq, C, Ev)
E = get_CR_strain(m_neq, n_neq, C);
Ee = E - Ev;
out = mu_neq...
    * ( Ee(1,1)*Ee(1,1) + Ee(1,2)*Ee(1,2) + Ee(1,3)*Ee(1,3)...
      + Ee(2,1)*Ee(2,1) + Ee(2,2)*Ee(2,2) + Ee(2,3)*Ee(2,3)...
      + Ee(3,1)*Ee(3,1) + Ee(3,2)*Ee(3,2) + Ee(3,3)*Ee(3,3));
end