function plot_results(paras, num_eq, num_neq,...
                            Ft_1, time_1, lambda1_exp_1, P1_exp_1,...
                            Ft_2, time_2, lambda1_exp_2, P1_exp_2,...
                            Ft_3, time_3, lambda1_exp_3, P1_exp_3,...
                            Ft_4, time_4, lambda1_exp_4, P1_exp_4,...
                            Ft_5, time_5, lambda1_exp_5, P1_exp_5)

[mu_eq, m_eq, n_eq, mu_neq, m_neq, n_neq, eta_d] = paras_to_array(paras, num_eq, num_neq);

P1_list_1 = get_P1_list(mu_eq, m_eq, n_eq, mu_neq, m_neq, n_neq, eta_d, Ft_1, time_1);
P1_list_2 = get_P1_list(mu_eq, m_eq, n_eq, mu_neq, m_neq, n_neq, eta_d, Ft_2, time_2);
P1_list_3 = get_P1_list(mu_eq, m_eq, n_eq, mu_neq, m_neq, n_neq, eta_d, Ft_3, time_3);
P1_list_4 = get_P1_list(mu_eq, m_eq, n_eq, mu_neq, m_neq, n_neq, eta_d, Ft_4, time_4);
P1_list_5 = get_P1_list(mu_eq, m_eq, n_eq, mu_neq, m_neq, n_neq, eta_d, Ft_5, time_5);

figure;

ax = axes('Position', [0.1 0.6 0.8 0.4], 'Box', 'on');

plot(ax, lambda1_exp_1(1:50:end), P1_exp_1(1:50:end), 'Color', '#003f5c', 'Marker', 'o', 'MarkerFaceColor', '#003f5c', 'MarkerSize', 12, 'LineStyle', 'none');
hold(ax, 'on');
plot(ax, lambda1_exp_1, P1_list_1, 'linewidth', 3.0, 'Color', '#003f5c', 'LineStyle', '-');
hold(ax, 'on');
plot(ax, lambda1_exp_2(1:10:end), P1_exp_2(1:10:end), 'Color', '#58508d', 'Marker', 'o', 'MarkerFaceColor', '#58508d', 'MarkerSize', 12, 'LineStyle', 'none');
hold(ax, 'on');
plot(ax, lambda1_exp_2, P1_list_2, 'linewidth', 3.0, 'Color', '#58508d', 'LineStyle', '-');
hold(ax, 'on');
plot(ax, lambda1_exp_3(1:100:end), P1_exp_3(1:100:end), 'Color', '#bc5090', 'Marker', 'o', 'MarkerFaceColor', '#bc5090', 'MarkerSize', 12, 'LineStyle', 'none');
hold(ax, 'on');
plot(ax, lambda1_exp_3, P1_list_3, 'linewidth', 3.0, 'Color', '#bc5090', 'LineStyle', '-');
hold(ax, 'on');
plot(ax, lambda1_exp_4(1:5:end), P1_exp_4(1:5:end), 'Color', '#ff6361', 'Marker', 'o', 'MarkerFaceColor', '#ff6361', 'MarkerSize', 12, 'LineStyle', 'none');
hold(ax, 'on');
plot(ax, lambda1_exp_4, P1_list_4, 'linewidth', 3.0, 'Color', '#ff6361', 'LineStyle', '-');
hold(ax, 'on');
plot(ax, lambda1_exp_5(1:3:end), P1_exp_5(1:3:end), 'Color', '#ffa600', 'Marker', 'o', 'MarkerFaceColor', '#ffa600', 'MarkerSize', 12, 'LineStyle', 'none');
hold(ax, 'on');
plot(ax, lambda1_exp_5, P1_list_5, 'linewidth', 3.0, 'Color', '#ffa600', 'LineStyle', '-');

xlabel(ax, 'Stretch', 'interpreter', 'latex', 'FontSize', 30, 'FontWeight', 'bold', 'FontName', 'Helvetica');
ylabel(ax, 'Nominal stress', 'interpreter', 'latex', 'FontSize', 30, 'FontWeight', 'bold', 'FontName', 'Helvetica');

set(ax, 'TickDir', 'out', ...
    'TickLength', [.02 .02], ...
    'XMinorTick', 'on', ...
    'YMinorTick', 'on', ...
    'YGrid', 'on', ...
    'XGrid', 'on', ...
    'XColor', [0 0 0], ...
    'YColor', [0 0 0], ...
    'LineWidth', 2, ...
    'FontSize', 25, 'FontWeight', 'bold');

l = legend(ax, 'exp-0d1', 'fit-0d1',...
               'exp-0d3', 'fit-0d3',...
               'exp-0d03', 'fit-0d03',...
               'exp-0d6', 'fit-0d6',...
               'exp-1', 'fit-1',...
               'location', 'southeast', 'Orientation', 'horizontal');
set(l, 'interpreter', 'latex', 'fontsize', 25, 'box', 'off', 'FontWeight', 'bold', 'FontName', 'Helvetica', 'NumColumns', 4);

X = 40.0;
Y = 40.0;
xMargin = 3;
yMargin = 3;
xSize = X - 2 * xMargin;
ySize = Y - 2 * yMargin;
set(gcf, 'Units', 'centimeters', 'Position', [5 5 xSize ySize]);
set(gcf, 'PaperUnits', 'centimeters');
set(gcf, 'PaperSize', [X Y]);
set(gcf, 'PaperPosition', [xMargin yMargin xSize ySize]);
set(gcf, 'PaperOrientation', 'portrait');

% print quality of fit
chi = get_quality_of_fit(P1_list_1, P1_exp_1)...
    + get_quality_of_fit(P1_list_2, P1_exp_2)...
    + get_quality_of_fit(P1_list_3, P1_exp_3)...
    + get_quality_of_fit(P1_list_4, P1_exp_4)...
    + get_quality_of_fit(P1_list_5, P1_exp_5);
text_chi = sprintf('$\\chi^2 = %.4g$', chi);
text(2.05, 0.1, text_chi, ...
        'HorizontalAlignment', 'center', ...
        'VerticalAlignment', 'bottom', ...
        'Interpreter', 'latex', ...
        'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');

% print parameters
text_mu_eq = cell(length(mu_eq), 1);
text_m_eq = cell(length(m_eq), 1);
text_n_eq = cell(length(n_eq), 1);

x_location = 1.0;
for ii = 1:length(mu_eq)
    text_mu_eq{ii} = sprintf('$\\mu_%d^{\\infty} = %.4g$', ii, mu_eq(ii));
    text(x_location, -4.0, text_mu_eq{ii}, ...
        'HorizontalAlignment', 'center', ...
        'VerticalAlignment', 'bottom', ...
        'Interpreter', 'latex', ...
        'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');
    x_location = x_location + 0.07;

    text_m_eq{ii} = sprintf('$m_%d^{\\infty} = %.4g$', ii, m_eq(ii));
    text(x_location, -4.0, text_m_eq{ii}, ...
        'HorizontalAlignment', 'center', ...
        'VerticalAlignment', 'bottom', ...
        'Interpreter', 'latex', ...
        'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');
    x_location = x_location + 0.07;

    text_n_eq{ii} = sprintf('$n_%d^{\\infty} = %.4g$', ii, n_eq(ii));
    text(x_location, -4.0, text_n_eq{ii}, ...
        'HorizontalAlignment', 'center', ...
        'VerticalAlignment', 'bottom', ...
        'Interpreter', 'latex', ...
        'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');
    x_location = x_location + 0.07;
end

text_mu_neq = cell(length(mu_neq), length(eta_d));
text_m_neq = cell(length(m_neq), length(eta_d));
text_n_neq = cell(length(n_neq), length(eta_d));
text_eta = cell(length(eta_d), 1);
y_location = -6.0;
for ii = 1:length(eta_d)
    x_location = 1.0;
    text_eta{ii} = sprintf('$\\eta_{\\mathrm{D}}^%d = %.4g$', ii, eta_d(ii));
    text(x_location, y_location, text_eta(ii), ...
        'HorizontalAlignment', 'center', ...
        'VerticalAlignment', 'bottom', ...
        'Interpreter', 'latex', ...
        'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');
    y_location = y_location - 2.0;
    for jj = 1:size(mu_neq,1)
        text_mu_neq{ii}{jj} = sprintf('$\\mu_{\\mathrm{neq}\\:%d}^{%d} = %.4g$', jj, ii, mu_neq(jj,ii));
        text(x_location, y_location, text_mu_neq{ii}{jj}, ...
            'HorizontalAlignment', 'center', ...
            'VerticalAlignment', 'bottom', ...
            'Interpreter', 'latex', ...
            'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');
        x_location = x_location + 0.08;

        text_m_neq{ii}{jj} = sprintf('$m_{\\mathrm{neq}\\:%d}^{%d} = %.4g$', jj, ii, m_neq(jj,ii));
        text(x_location, y_location, text_m_neq{ii}{jj}, ...
            'HorizontalAlignment', 'center', ...
            'VerticalAlignment', 'bottom', ...
            'Interpreter', 'latex', ...
            'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');
        x_location = x_location + 0.08;

        text_n_neq{ii}{jj} = sprintf('$n_{\\mathrm{neq}\\:%d}^{%d} = %.4g$', jj, ii, n_neq(jj,ii));
        text(x_location, y_location, text_n_neq{ii}{jj}, ...
            'HorizontalAlignment', 'center', ...
            'VerticalAlignment', 'bottom', ...
            'Interpreter', 'latex', ...
            'FontSize', 25, 'FontWeight', 'bold', 'Color', 'k', 'FontName', 'Helvetica');
        x_location = x_location + 0.08;

    end
    y_location = y_location - 2.0;
end
end